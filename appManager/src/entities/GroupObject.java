package entities;

public class GroupObject
{
    private int groupID;

    private int idProf;

    /**
     * @return the groupID
     */
    public int getGroupID()
    {
        return groupID;
    }

    /**
     * @param groupID
     *            the groupID to set
     */
    public void setGroupID( int groupID )
    {
        this.groupID = groupID;
    }

    /**
     * @return the idProf
     */
    public int getIdProf()
    {
        return idProf;
    }

    /**
     * @param idProf
     *            the idProf to set
     */
    public void setIdProf( int idProf )
    {
        this.idProf = idProf;
    }
}
